public class Homework03 {
    public static void main(String[] args) {
        /**
         * 保存两本书名，用+拼接，看效果。
         */
        String book1 = "天龙";
        String book2 = "笑傲";
        System.out.println(book1 + book2);
        /**
         * 保存两个性别，用+拼接看效果。
         */
        char c1 = '男';
        char c2 = '女';
        System.out.println(c1);
        System.out.println(c2);
        System.out.println(c1 + c2);
        /**
         * 保存两本书价格，用+拼接，看效果
         */
        double price1 = 33.33;
        double price2 = 44.44;
        System.out.println(price1 + price2);
    }
}
