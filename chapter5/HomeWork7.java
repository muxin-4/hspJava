
public class HomeWork7 {

    public static void main(String[] args) {
        // 7.输出小写的a-z以及大写的Z-A

        for (char c1 = 'a'; c1 <= 'z'; c1++) {
            System.out.print(c1 + " ");
        }
        System.out.println();
        for (char d1 = 'Z'; d1 >= 'A'; d1--) {
            System.out.print(d1 + " ");
        }
    }
}
